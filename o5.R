cRep <- function (List, Data){
  
  for (i in 1:length(List)){
    word = List[i]
    a <- grep(word, Data)
    Data[a] <- paste(Data[a],"#")
  }
  
  Data <- gsub( '\\w', '', Data)
  Data <- gsub( '#', '1', Data)
  Data <- gsub( '\\D', '', Data)
  Data <- gsub( '1111', '4', Data)
  Data <- gsub( '111', '3', Data)
  Data <- gsub( '11', '2', Data)
  Data <- as.numeric(Data)
  Data[is.na(Data)] <- 0
  
  return (Data)
}

